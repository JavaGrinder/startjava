public class VariableNamesTheme {
    
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");

        //Цифра
        char digit = '1';
        System.out.println("цифра = " + digit);

        //Сумма чисел
        int sumNums = 5;
        System.out.println("сумма чисел = " + sumNums);

        //Произведение цифр
        int productDigits = 6;
        System.out.println("произведение цифр = " + productDigits);

        //Максимальное число
        int maxNum = 2147483647;
        System.out.println("максимальное число = " + maxNum);

        //Количество десятков
        int quantityTens = 7;
        System.out.println("количество десятков = " + quantityTens);

        //Вес собаки
        byte dogWeight = 15;
        System.out.println("вес собаки = " + dogWeight + " килограмм");

        //Исходное число
        int sourceNum = 0;
        System.out.println("исходное число = " + sourceNum);

        //Процент по вкладу
        float interestDeposit = 14.5F;
        System.out.println("процент по вкладу = " + interestDeposit + "%");

        //(переменная хранит символ) &
        char ampersand = '&';
        System.out.println("Символ амперсанд = " + ampersand);

        //Код ошибки
        int errorCode = 404;
        System.out.println("код ошибки = " + errorCode);

        //Тип сообщения
        byte messageType = 2;
        System.out.println("тип сообщения = " + messageType);

        //Число нулей
        byte quantityZeros = 10;
        System.out.println("число нулей = " + quantityZeros);

        //Уникальное число
        int uniqueNum = 1;
        System.out.println("уникальное число = " + uniqueNum);

        //Случайное число
        int randomNum = 11;
        System.out.println("случайное число = " + randomNum);

        //Математическое выражение
        int mathExpression = 7;
        System.out.println("математическое выражение = " + mathExpression);

        //Выбор (чего-либо)
        int selection = 3;
        System.out.println("выбор = " + selection);

        //Счет (в игре)
        int score = 100;
        System.out.println("счет = " + score);

        //Максимальная длина (строки)
        byte maxLength = 120;
        System.out.println("максимальная длина = " + maxLength);

        //Пункт меню
        byte menuItem = 3;
        System.out.println("пункт меню = " + menuItem);

        //Стоимость кофе на вынос
        float priceCoffeeToGo = 99.99F;
        System.out.println("cтоимость кофе на вынос = " + priceCoffeeToGo + " рублей");

        //Дата начала (чего-либо)
        float startDate = 18.01F;
        System.out.println("дата начала = " + startDate);

        //Окончание диапазона
        int endRange = 10;
        System.out.println("окончание диапазона = " + endRange);

        //Полное имя работника месяца
        byte fullNameEmployeeMonth = 1;
        System.out.println("полное имя работника месяца = " + fullNameEmployeeMonth);

        //Заголовок электронной книги
        byte ebookTitle = 4;
        System.out.println("заголовок электронной книги = " + ebookTitle);

        //Размер
        float size = 146.5F;
        System.out.println("размер = " + size);

        //Вместимость (чего-либо)
        byte capacity = 100;
        System.out.println("вместимость = " + capacity);

        //Счетчик
        int counter = 1000;
        System.out.println("счетчик = " + counter);

        //Путь до файла
        char pathToFile = '5';
        System.out.println("путь до файла = " + pathToFile);

        //Количество чисел в каждой строке
        int quantityNumsEachLine = 101;
        System.out.println("количество чисел в каждой строке = " + quantityNumsEachLine);

        System.out.println('\n' + "2. Boolean-переменные");

        //Сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("сотни равны? = " + isEqualHundreds);

        //Компьютер включен?
        boolean isPcOn = true;
        System.out.println("компьютер включен? = " + isPcOn);

        //Есть равные цифры?
        boolean isEqualDigits = false;
        System.out.println("есть равные цифры? = " + isEqualDigits);

        //(что-либо) создано?
        boolean isCreated = false;
        System.out.println("создано? = " + isCreated);

        //(что-либо) пустое?
        boolean isEmpty = true;
        System.out.println("пустое? = " + isEmpty);

        //(что-либо) активное?
        boolean isActive = true;
        System.out.println("активное? = " + isActive);

        //Новый?
        boolean isNew = true;
        System.out.println("новый? = " + isNew);

        //Электронная почта действительная?
        boolean isEmailValid = true;
        System.out.println("электронная почта действительная? = " + isEmailValid);

        //Имеются уникальные строки?
        boolean hasUniqueStrings = false;
        System.out.println("имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println('\n' + "3. Аббревиатуры");

        //Старый universally unique identifier
        byte oldUuid = 1;
        System.out.println("старый universally unique identifier = " + oldUuid);

        //Производитель оперативной памяти
        byte ramMaker = 7;
        System.out.println("производитель оперативной памяти = " + ramMaker);

        //Емкость жесткого диска
        int hddSpace = 512;
        System.out.println("емкость жесткого диска = " + hddSpace + " GB");

        //Протокол передачи гипертекста
        byte http = 2;
        System.out.println("протокол передачи гипертекста = " + http);

        //Сокращенный uniform resource locator
        byte shortUrl = 5;
        System.out.println("сокращенный uniform resource locator = " + shortUrl);

        //Новый идентификатор клиента
        byte newClientId = 3;
        System.out.println("новый идентификатор клиента = " + newClientId);

        //Кодировка american standard code for information interchange
        char codeAscii = 'a';
        System.out.println("кодировка american standard code for information interchange = "
                + codeAscii);
    }
}